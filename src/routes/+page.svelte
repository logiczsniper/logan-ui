<h2>Progolue</h2>
<p>These are not poems. I'm not a poet.</p>
<p>
  I sometimes get attached to ideas. Since my early teenage years, certain
  phrases, expressions, words, patterns, parallels - would resonate inside my
  brain. They would pique my interest, seeming strangely <i>poetic</i> to me.
  And then I would move on. Because I’m not a poet. Between the two buckets of
  poets and non-poets, I have never once thought I was in the former. I never
  particularly liked poetry, until around the end of my schooling. The work of
  world-class poets had finally changed my views on poetry. However, I still
  didn’t read in my free time and I still didn’t feel inspired to try creating
  my own poetry. Because I’m not a poet. This is my bucket...
  <small>but who put me here?</small>
</p>
<p>
  Years passed and I’m still not a poet. The occasional, momentary flashes of
  curiosity continued. But I started writing these down. I wanted to extend the
  expiration date on these brief thoughts, and maybe, just maybe, one day call
  back to each of them, and let my mind roam, and maybe write something. Not a
  poem, because I’m not a poet.
</p>
<p>
  This continued for awhile, with no outcomes, until one morning, I was thinking
  about how does one start to write. At this point, I had built up quite the
  collection of intriguing thoughts that I wanted to enjoy. But unlike 1906
  where it might’ve been normal to go watch some swans and write a poem about
  it, I had no idea what to do. But what is normal? And who cares? I realized, I
  can just do this for me. That realization, and one more idea, pushed me to
  finally take my first step. The idea of enhancing my work (not poems) using
  the modern web. Ideas flooded my head with how I could use JavaScript to
  present my work (again, not poems) the way that I want to. I don’t know if
  others have done this, but I don’t care. What I want to create here, to me, is
  art. It is unique, to me.
</p>
<p>
  So, after years of thinking about doing something, I actually did something. I
  don’t know if I’m doing it adequately, or even correctly, but such things
  don’t matter to me now. I’m just gonna do.
</p>
